// Fungsi untuk mendapatkan terjemahan
function getTranslation(key, lang = 'id') {
    if (!translations[lang] || !translations[lang][key]) {
        // Jika tidak ada terjemahan, kembalikan kunci asli
        return key;
    }
    return translations[lang][key];
}

// Fungsi untuk menerjemahkan seluruh halaman
function translatePage(lang = 'id') {
    const elements = document.querySelectorAll('[data-translate]');
    
    elements.forEach(element => {
        const key = element.getAttribute('data-translate');
        
        if (key) {
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                if (element.getAttribute('placeholder')) {
                    element.setAttribute('placeholder', getTranslation(key, lang));
                } else {
                    element.value = getTranslation(key, lang);
                }
            } else {
                element.textContent = getTranslation(key, lang);
            }
        }
    });
    
    // Simpan bahasa pilihan pengguna di localStorage
    localStorage.setItem('language', lang);
    
    // Update bahasa aktif di UI
    updateLanguageSelector(lang);
}

// Fungsi untuk menginisialisasi modul terjemahan
function initTranslations() {
    // Cek apakah ada bahasa yang disimpan di localStorage
    const savedLang = localStorage.getItem('language') || 'id';
    
    // Tambahkan pendengar acara untuk pemilih bahasa
    const langSelector = document.getElementById('languageSelector');
    if (langSelector) {
        langSelector.value = savedLang;
        langSelector.addEventListener('change', function() {
            translatePage(this.value);
        });
    }
    
    // Terjemahkan halaman ke bahasa yang disimpan
    translatePage(savedLang);
}

// Fungsi untuk memperbarui UI pemilih bahasa
function updateLanguageSelector(lang) {
    const langSelector = document.getElementById('languageSelector');
    if (langSelector) {
        langSelector.value = lang;
    }
}

// Inisialisasi ketika dokumen siap
document.addEventListener('DOMContentLoaded', function() {
    initTranslations();
});
